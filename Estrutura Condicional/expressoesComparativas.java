public class expressoesComparativas {
    public static void main(String[] args) {

        /*
         * (>) - Maior
         * (<) - Menor
         * (>=) - Maior ou igual
         * (<=) - Menor ou igual
         * (==) - Igual
         * (!=) - Diferente
         */

        int x = 5;
        if (x > 6) {
            System.out.println("Verdadeiro!");
        }
        else {
            System.out.println("Falso!");
        }

    }
}
